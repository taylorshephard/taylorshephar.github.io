function nonFriends(name, array) {
    //declare notFriends set to an array
    var notFriends = [];
    
    //use a for loop to get access to each person in array
    for (var i = 0; i < array.length; i++) {
        //check if the person doesn't have the name in their 
        //friends array
            //push that person into the notFriends array
        if (array[i].name !== name && array[i].friends.indexOf(name) === -1) {
            // console.log(array[i])
            notFriends.push(array[i].name);
        }
    }
    //return notFriends
    return notFriends;
}

