    //declare result set to an array
    var result = [];
    //get access to each value in array using for loop
    for (var i = 0; i < array.length; i++) {
        //check if result does not have the value using includes
            //if true push value into result
        if (result.includes(array[i]) === false) {
            result.push(array[i]);
        }
    }
    // return result
    return result;
